
const { PrismaClient } = require('@prisma/client')

const prisma = new PrismaClient()

async function main() {
    const suckerRemovalId = 'c8cc7965-d4d6-4b12-8f0a-e6a0c66d7ca5'

    const richMarkdown = `
### 🎯 作業目的
株のフォーメーションを整え、不要な側枝（ヤゴ）を除去することで、花形とボリュームを安定させます。
> **💡 ヤゴかきは「見た目の完成度」を決める重要な工程です。**

#### 🚀 目標
- 主枝を明確にし、ボリュームを維持しつつ過繁茂を防ぐ。
- 出荷時にスリーブ内へ収まる理想的な枝構成にする。

---

### 📋 基本方針
- **効率重視**: 非常に重要な作業ですが、時間をかけすぎない（1本あたりの時間を意識する）。
- **人件費管理**: 時間をかけすぎると採算割れのリスクがあります。
- **品種評価**: ヤゴの発生が極端に多い品種は、次作の不採択を検討する基準とします。

---

### 🔍 発生傾向の確認
- 通路側や光が当たる側に多く発生します。
- 下位節から多く発生するタイプなど、品種による特性を把握してください。

---

### 📋 作業手順

#### 1. 株全体の確認
- 主枝を確認し、どの枝を残すべきか瞬時に判断します。

#### 2. 側枝除去
- 不要なヤゴを丁寧に摘み取ります。
- **注意**: 強く引っ張って茎を傷つけないよう注意してください。

#### 3. 下位多発型の処理
- 下から多く発生する品種は、下葉と一緒に「すっこく（引き取る）」ように処理します。
- 中途半端に残さないことがポイントです。

#### 4. フォーメーション確認
- 最終的にスリーブ内に収まる枝数か、花上部が揃う構造になっているかを確認します。

---

### ✅ 判断基準
- [ ] 主枝が明確になっているか
- [ ] 横張りしすぎていないか
- [ ] 収穫時に枝が暴れない構成か

---

### 📉 未実施・不備のリスク
- **作業不足**: 花頭のバラつき、横広がり、スリーブに入らない、出荷調整時間の増大。
- **過剰実施**: 作業時間の増大、人件費増による採算悪化。
  - > **⚠️ 鉄則**: 「完璧」ではなく「十分」な状態を目指してください。

### 💎 品質・利益への影響
- **品質**: ヤゴ多すぎ → ボリューム過多 → 優品落ち / 整枝不足 → 花揃いの悪化。
- **利益**: 1株あたり数秒の差が、10a単位では数時間のコスト差に直結します。
`.trim()

    await prisma.workManual.update({
        where: { id: suckerRemovalId },
        data: {
            purpose: '株のフォーメーションを整え、花形・ボリュームを安定させ、スリーブに収まる樹形にする。',
            timingStandard: '草丈伸長期。目標: 10aあたり約25時間。1本あたりの秒数を意識する。',
            actionSteps: richMarkdown,
            riskIfNotDone: '花頭バラつき、横広がり、スリーブ不可、出荷調整時間増',
            requiredTime10a: 25,
            difficultyLevel: 4,
            impactOnQuality: 'ボリューム過多による優品落ち、花揃い悪化',
            impactOnYield: '出荷調整時間の増減に直結。1株数秒の差が数時間の利益差になる',
            inputParameters: JSON.stringify({
                notes: ['主枝の明確化', 'スリーブへの収まり性', '適正な作業時間']
            })
        }
    })

    console.log(`Updated "Sucker Removal" manual with rich markdown: ${suckerRemovalId}`)
}

main()
    .catch(console.error)
    .finally(async () => {
        await prisma.$disconnect()
    })
