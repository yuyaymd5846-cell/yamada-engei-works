
const { PrismaClient } = require('@prisma/client')

const prisma = new PrismaClient()

async function main() {
    const cultivationMgmtId = '05549cf2-0237-44e1-95f7-fda7063bd620'

    const richMarkdown = `
### 🎯 作業目的
生育を安定させ、病害虫や設備トラブルを未然に防止します。
> **💡 栽培管理は「事故防止と品質安定」を担う、農園運営の中枢工程です。**

#### 🚀 目標
- 毎日欠かさずハウス内を確認し、異常の兆候を早期に発見する。
- 病害虫を初期段階で叩き、拡大を最小限に抑える。
- 設備トラブル（加温機・潅水等）による事故ゼロを目指す。
- 草丈を計画範囲内で維持し、最適なタイミングでネット上げを行う。

---

### 📋 基本方針「観察がすべて」
- 毎日必ずハウスに入り、**「異常がないか」**を探す意識で観察します。
- わずかな変化に気づく観察力が、最終的な収益を守ります。

---

### 🔍 日常確認項目

#### 1. 病害虫チェック
- [ ] 害虫の有無を葉の表裏、成長点、蕾付近で確認します。
- [ ] 葉に病斑（褐斑病、白さび病等）がないか丹念にチェックします。
- > **早期発見 ＝ 被害の最小化 ＝ 薬剤コストの抑制**

#### 2. 設備チェック
- [ ] **加温機**: 設定温度通りか、異音はないか。
- [ ] **換気・カーテン**: 動作に異常はないか、破れや引っかかりはないか。
- [ ] **潅水設備**: ノズルの詰まりや水漏れがないか。

#### 3. 土壌水分確認
- [ ] 群落中央部や端など、複数箇所で土の乾き具合を確認します。
- [ ] 極端な乾燥や過湿を避け、潅水タイミングを判断します。

---

### 📏 ネット上げ管理

| ステージ | 管理ポイント | 注意事項 |
| :--- | :--- | :--- |
| **定植〜14日目** | ほぼ動きなし | 活着を優先し、根張りを観察 |
| **活着後〜蕾形成前** | 伸長スピード増 | **重要**: 定期的にネットを上げます |
| **消灯以降** | 最大伸長 | 草丈に合わせ、首が曲がらないよう調整 |

> **⚠️ ネット上げの遅れ**：茎の曲がり、フォーメーションの乱れを引き起こし、致命的な「優品落ち」に繋がります。

---

### 📉 未実施・不備のリスク
- 病害の爆発的拡大、設備停止による全損事故、草丈の極端なバラつき。
- ネット上げ遅れによる「曲がり」の増加。

### 💎 利益への影響
- 初期の異常放置は、後に全面被害（全滅リスク）となり得ます。
- **「最も地味だが、最も利益に効く工程」**として、日々の観察を徹底してください。
`.trim()

    await prisma.workManual.update({
        where: { id: cultivationMgmtId },
        data: {
            purpose: '生育安定、病害虫早期発見、設備トラブル防止、計画的な草姿維持。事故防止の中枢。',
            timingStandard: '定植後〜収穫前。毎日巡回。目標: 10aあたり30時間/作',
            actionSteps: richMarkdown,
            riskIfNotDone: '病害拡大、草丈バラつき、ネット遅れによる曲がり、潅水遅れによる停滞',
            requiredTime10a: 30,
            difficultyLevel: 4,
            impactOnQuality: '曲がり防止、花揃いの安定。異常の早期発見が秀率を左右する',
            impactOnYield: '壊滅的な被害（全損）の未然防止。初期対応による被害最小化',
            impactOnProfit: '最も地味だが、最も利益に効く工程。小さな異常の放置は巨大な損失へ',
            inputParameters: JSON.stringify({
                check_items: ['病害虫', '設備異常', '土壌水分', 'ネット位置'],
                frequency: '毎日'
            })
        }
    })

    console.log(`Updated "Cultivation Management" manual with rich markdown: ${cultivationMgmtId}`)
}

main()
    .catch(console.error)
    .finally(async () => {
        await prisma.$disconnect()
    })
