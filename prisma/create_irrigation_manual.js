
const { PrismaClient } = require('@prisma/client')
const { v4: uuidv4 } = require('uuid')

const prisma = new PrismaClient()

async function main() {
    const richMarkdown = `
### 🎯 作業目的
根域水分を適正に維持し、草勢を安定させます。かん水は「草姿設計そのもの」であり、秀率と収量を最大化するための最重要工程です。

#### 🚀 目標
- 過湿にせず、乾かしすぎない絶妙なバランス。
- 生育段階に応じた正確な水量調整。
- 天候と土壌状態を観察し、的確な判断を行う。

---

### 🌊 かん水方式と注意事項

#### 1. マスプレーかん水（ホース引き式）
- **作業のポイント**:
  - [ ] ホースが杭に引っかからないようルートを確認する。
  - [ ] 定植直後は、水圧で苗をなぎ倒さないよう細心の注意を払う。
  - [ ] 30分〜1時間ごとに正常な稼働とワイヤーの張りをチェックする。
- **目安時間**: 通常期 60〜90分 / 消灯以降 約3時間

#### 2. スピンネットかん水（散水式）
- **作業のポイント**:
  - [ ] 水圧が適正か確認する（出方が大きく変わるため）。
  - [ ] 各スプリンクラーの破損や散水ムラがないかチェックする。
- **目安時間**: 通常期 10分 / 消灯以降 約15分

---

### 📅 生育ステージ別かん水基準

| ステージ | 頻度（目安） | 降雨水量換算 | 目的・備考 |
| :--- | :--- | :--- | :--- |
| **定植直後（活着期）** | ほぼ毎日 | 約3mm | 少量多かん水で根張り促進 |
| **定植2週間後** | 週1〜2回 | 約5mm | 発根の安定を目指す |
| **消灯直後** | 週1回程度 | 1回量を増やす | 頻度は下げ、浸透を深くする |
| **消灯以降** | 適宜 | 20〜30mm | 1回量を最大化する |

---

### ✅ 判断基準「観察がすべて」
- 土壌の水分の含み具合、表層の乾き具合
- 葉のしおれ具合（朝夕の観察が重要）
- 草姿の勢い、およびその日の日射・気温予報

---

### 📉 未実施・判断ミスのリスク
- **不備**: 根傷み、立枯病の発生、茎径・草丈不足、花が小さくなる。
- **影響**: 水分ムラはそのまま草姿ムラになり、秀率を低下させます。

### 💎 利益への影響
- かん水判断が収量そのものです。過湿による欠株や、茎径不足による優品落ちは、直接的に売上を減少させます。
`.trim()

    const newManual = await prisma.workManual.create({
        data: {
            id: uuidv4(),
            workName: 'かん水',
            stage: '全期間',
            purpose: '根域水分を適正に維持し、草勢と秀率を最大化する（草姿設計そのもの）。',
            timingStandard: '天候・生育ステージに応じた調整。目標: 10aあたり15時間/作',
            actionSteps: richMarkdown,
            riskIfNotDone: '根傷み、立枯病、茎径不足、草丈不足、花小さくなる、秀率低下',
            requiredTime10a: 15,
            difficultyLevel: 5,
            impactOnQuality: '水分ムラ＝草姿ムラ。茎径不足による優品落ち',
            impactOnYield: '過湿による欠株増、収量低下。かん水判断が収量に直結する',
            impactOnProfit: '収量と高品質（秀品）維持による利益の最大化。判断ミスは直接的な損失を招く',
            inputParameters: JSON.stringify({
                methods: ['マスプレーかん水', 'スピンネットかん水'],
                monitoring: ['土壌水分', '葉のしおれ', '天候予報']
            })
        }
    })

    console.log(`Created new "Irrigation" manual: ${newManual.id}`)
}

main()
    .catch(console.error)
    .finally(async () => {
        await prisma.$disconnect()
    })
