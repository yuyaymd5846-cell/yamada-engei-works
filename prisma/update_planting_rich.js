
const { PrismaClient } = require('@prisma/client')

const prisma = new PrismaClient()

async function main() {
    const plantingId = 'adf0e8c2-8a42-4208-8d15-ab3cd2e49b64'

    const richMarkdown = `
### 🎯 作業目的
活着を揃えて初期生育を安定させるとともに、株間・密度を正確に守ることで均一な草姿を作り上げます。
> **💡 定植精度＝草丈揃い＝秀2L率に直結します！**

#### 🚀 目標
- **欠株ゼロ**
- 活着遅れを出さない
- 植えムラを作らない
- 指示本数を正確に守る

---

### 🛠 準備するもの
- [ ] **菊苗**
- [ ] **パレット**
- [ ] **手袋**（任意）

---

### 📋 手順と注意点

#### 1. 圃場の最終確認
- 金網のマス目と当日指示の植付本数を必ず確認します。
- 列の最初と最後の2列は、管理スペース確保のため空けておきます。
- > **⚠️ 警告**: 密度の間違いは、草姿の乱れ、2L率の低下、歩留まり悪化に直結します。

#### 2. 定植準備（広げとチェック）
- パレットから苗を数個ずつマス目上に広げます。
- **チェック事項**:
  - 苗がスムーズに抜けるか（土が付いてこないか）
  - 明らかに萎れている苗はないか
- ※ 不良苗はマス目の外へ除外します。根鉢に土が多少残る程度なら問題ありません。

#### 3. 植え付けの技術
- 定植図に基づき、マス目の中心へ正確に植えます。
- 植え終わったら上から軽く押さえて土を馴染ませます。
- **注意ポイント**:
  - **浮き苗**（根が浮いている状態）にしない。
  - **深植え**しすぎない。
  - 土盛りが斜めの場合でも、垂直に正しく植え付けます。

#### 4. 後片付け
- **残土処理**: 土壌改良のため、残った土はマス目内へ戻します。
- **パレット整理**: 通路に一時的に置き、最後にまとめて入口へ運びます。
- **搬出**: トラック積載時は正しい向きで、**5段重ね**を徹底します。

---

### 📉 未実施・精度不足のリスク
- 活着遅延、欠株の増加、草丈のバラつき、秀率の低下
- 後工程（ヤゴかき・収穫）の時間増大

### 💎 品質・利益への影響
- **品質**: 株間不揃いによる茎径差、深植えによる生育停滞、浅植えによる倒伏
- **利益**: 欠株による出荷箱数の減少、バラつきによるL・M落ちの増加、および人件費の増大
`.trim()

    await prisma.workManual.update({
        where: { id: plantingId },
        data: {
            purpose: '活着を揃え、初期生育を安定させるとともに、正確な密度で均一な草姿を作る。',
            timingStandard: '目標: 欠株ゼロ、活着遅れなし、指示本数を正確に守る',
            actionSteps: richMarkdown,
            riskIfNotDone: '活着遅延、欠株増加、草丈バラつき、秀率低下、後工程時間増',
            requiredTime10a: 46,
            difficultyLevel: 3,
            impactOnQuality: '株間不揃い → 茎径差 / 深植え → 生育停滞 / 浅植え → 倒伏',
            impactOnYield: '欠株による出荷減、バラつきによるL・M落ち増加、作業コスト増',
            inputParameters: JSON.stringify({
                tools: ['菊苗', 'パレット', '手袋']
            })
        }
    })

    console.log(`Updated "Planting" manual with rich markdown: ${plantingId}`)
}

main()
    .catch(console.error)
    .finally(async () => {
        await prisma.$disconnect()
    })
