
const { PrismaClient } = require('@prisma/client')

const prisma = new PrismaClient()

async function main() {
    const shippingAdjustmentId = '42f34cb5-7d6e-4317-89ba-57710d81567e'

    const richMarkdown = `
### 🎯 作業目的
等級（秀・優・良）を正確かつ迅速に判定し、規格重量・規格長を満たした商品を作り上げます。
> **💡 出荷調整は、販売単価を最終的に決定する「利益確定」の工程です。**

#### 🚀 目標
- 主力規格である **秀2L** を安定して出荷する。
- 判定時間をかけすぎず、1本あたりの労務費を常に意識する。
- 荷姿を美しく仕上げ、市場評価を最大化する。

---

### 📋 等級判定基準（第一工程）
まず、**秀品・優品・良品**の判定を以下のポイントで行います。

| 判定ポイント | チェック内容 |
| :--- | :--- |
| **頂花下がり** | フォーメーションの崩れがないか |
| **曲がり具合** | 許容範囲内か、極端な曲がりがないか |
| **全体バランス** | 全体的な枝構成が美しいか |

> **⚠️ 注意**: 判別に迷い、時間をかけすぎないこと。基準外は速やかに優品・良品へ振り分けます。

---

### 📋 束ね工程

#### 1. 本数まとめ
- 茎の太さを揃えながら、以下の本数でまとめます。
  - **L以上**: 10本で1束
  - **M規格**: 20本で1束

#### 2. 押し切り
- 押し切り機を使用し、以下の長さに揃えます。
  - **秀品**: 80cm
  - **優品**: 70cm

#### 3. 結束・重量測定
- 輪ゴムで結束し、重量を測定します。
- **💡 ポイント**: 水揚げで約1割重量が増えることを加味して判断します。

---

### ⚖️ 重量基準（タイプⅠ）

| 規格 | 1本最低重量 | 束重量目安 |
| :--- | :--- | :--- |
| **2L（80本入）** | 66g | 10本で660g以上 |
| **2L（100本入）** | 45g | 10本で450g以上 |
| **L（100本入）** | 34g | 10本で340g以上 |
| **M（160本入）** | 24g | 20本で480g以上 |

*※例: 10本420gの場合 → 水揚げ後 約462g（2L 100本規格 適合）*

---

### 📦 スリーブ・水揚げ工程

#### 1. スリーブ工程
- 規格合格後、スリーブを通します。
- **美観**: 花頭を完璧に揃え、荷姿を美しく整えます。
  - > **荷姿 ＝ 市場評価 ＝ 販売単価**

#### 2. 水揚げ
- バケツに束をまとめ、立てた状態で**3時間以上**水揚げします。
- **冬期以外**: 水の腐敗防止のため、**ケミクロンGを1粒**投入してください。

---

### 📉 未実施・精度不足のリスク
- 優品落ちの増加、クレームの発生、市場単価の下落。
- 作業スピードの低下による出荷調整時間の増大。

### 💎 品質・利益への影響
- 1本あたりの判別スピードが数秒遅れるだけで、全体の労務費を大きく圧迫します。
- **「迷わない」「やり直しを減らす」**ことが、直接的な利益改善に繋がります。
`.trim()

    await prisma.workManual.update({
        where: { id: shippingAdjustmentId },
        data: {
            workName: '出荷調整（手作業）',
            purpose: '等級を正確かつ迅速に判定し、規格を満たした商品を作ることで販売単価を最大化する。',
            timingStandard: '収穫後。目標: 10aあたり178時間以内、1本あたりの労務費を意識。',
            actionSteps: richMarkdown,
            riskIfNotDone: '優品落ち、クレーム、単価下落、出荷調整時間増加',
            requiredTime10a: 178,
            difficultyLevel: 5,
            impactOnQuality: '荷姿＝市場評価＝単価。花頭の揃いや美観が極めて重要',
            impactOnYield: '判定ミスによる格下げの防止。重量基準の正確な運用',
            impactOnProfit: '出荷調整は単価を決める最終工程。1本数秒の差が利益を左右する',
            inputParameters: JSON.stringify({
                tools: ['押し切り機', '専用スリーブ', 'ケミクロンG', '輪ゴム'],
                standards: ['秀80cm', '優70cm', '重量基準タイプⅠ']
            })
        }
    })

    console.log(`Updated "Shipping Adjustment" manual with rich markdown: ${shippingAdjustmentId}`)
}

main()
    .catch(console.error)
    .finally(async () => {
        await prisma.$disconnect()
    })
