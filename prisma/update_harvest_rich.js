
const { PrismaClient } = require('@prisma/client')

const prisma = new PrismaClient()

async function main() {
    const harvestId = 'bc9952a7-69b3-4f09-a48f-cba7fc5bf204'

    const richMarkdown = `
### 📋 作業目的
1. **出荷調整の効率化**: 出荷調整がしやすい状態で花を搬入する。
2. **品質保護**: 花・葉・未収穫株を傷つけない。
3. **秀率の安定**: 切り前を揃えて秀率を安定させる。
4. **労働時間の抑制**: 作業効率を最大化し、労働時間を抑える。
5. **鮮度維持**: 日射ストレスを避け、花温上昇を防ぐ。

---

### 🔍 事前確認（作業開始前）
#### ① 切り前の目揃い
- [ ] 今日の切り前基準を全員で確認。
- [ ] 実物で目合わせを行う。
- [ ] 若すぎ／進みすぎの基準も明示する。

#### ② 日射確認・カーテン調整
- [ ] 強日射時は**上層カーテンを閉める**。
- [ ] 直射日光が花に当たらない環境で収穫する。
- [ ] 花温が上がらない状態を維持する。

> **背景**: 直射下での収穫は「花温上昇 → 収穫後しおれ → 秀率低下」につながります。

---

### 🛠 使用資材
- [ ] **台車**
- [ ] **モテモテシート**
- [ ] **収穫鎌**（切れ味が悪い場合は即交換！）
- [ ] **収穫爪**（根抜き品種用）

---

### 🚶 作業手順
#### STEP 1：台車準備
- 台車にモテモテシートをセットし、マジックテープ位置を確認して船間通路へ入ります。

#### STEP 2：収穫方向（奥から手前へ）
- 台車を引きながら**後退収穫**を行います。
- ※満載台車で未収穫花を傷つけないため。

#### STEP 3：収穫方法
- **基本**: 収穫鎌で切る。
  - *注意*: 切れ味低下は茎潰れ・導管損傷・日持ち低下の原因（鎌価格：2,000〜3,000円）。
- **根抜き品種**: そのまま引き抜き、根元を**収穫爪で折って**台車へ。

#### STEP 4：台車への置き方
1. ネットから丁寧に抜き取る。
2. **花頭を自分側**に向ける。
3. シート内・足元へ確実に収める（葉を擦らない、花を押し付けない）。

#### STEP 5：規格落ち予測品の分類（逆向き配置）
- 「曲がり」「長下がり」「規格落ち確定株」は**逆向き**に置きます。
- *目的*: 出荷調整時の選別時間短縮・見落とし防止。

#### STEP 6：搬出
- シート満杯でマジックテープを固定し、軽トラへ搬入。
- ※未収穫株を折らないよう、シート端での擦れに注意。

---

### 📅 収穫期間の目安
- **期間**: 約9日間（開花遅延がある場合は2週間以上）。
- **ピーク**: 開始から4〜5日目。
`.trim()

    await prisma.workManual.update({
        where: { id: harvestId },
        data: {
            purpose: '1.出荷調整の効率化 2.品質保護 3.秀率の安定 4.労働時間の抑制 5.鮮度維持',
            timingStandard: '今日の切り前基準の確認、強日射時のカーテン調整',
            actionSteps: richMarkdown,
            riskIfNotDone: '花温上昇、しおれ、秀率低下、品質低下',
            requiredTime10a: 85,
            difficultyLevel: 3,
            inputParameters: JSON.stringify({
                tools: ['台車', 'モテモテシート', '収穫鎌', '収穫爪']
            })
        }
    })

    console.log(`Updated manual with rich markdown: ${harvestId}`)
}

main()
    .catch(console.error)
    .finally(async () => {
        await prisma.$disconnect()
    })
