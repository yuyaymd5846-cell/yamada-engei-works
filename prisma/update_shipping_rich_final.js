
const { PrismaClient } = require('@prisma/client')

const prisma = new PrismaClient()

async function main() {
    const shippingId = 'dd190004-70cf-411d-bc71-d171aef43afa'

    const richMarkdown = `
### 🎯 作業目的
等級ごとに正しく箱詰めし、荷姿を整えることで、輸送中の花傷みを防ぎます。
> **💡 出荷は、お客様へ届ける前の「最後の品質管理工程」です。**

#### 🚀 目標
- 等級ごとに適正な箱（15cm/13cm）を使い分ける。
- 花頭が揃った美しい荷姿で、市場評価を維持する。
- 輸送中に傷まない完璧な梱包を行う。
- **食害・病害の混入ゼロ**を徹底する。

---

### 📦 出荷箱の種類と使用区分

| 箱サイズ | 使用階級・目的 |
| :--- | :--- |
| **15cm箱** | 2L、2L80、M、良品 |
| **13cm箱** | L |

> **⚠️ 注意**: 優品・2L80・Mについては、箱側面の表記を必ず確認してください。箱の間違いはクレームや信用の低下に直結します。

---

### 📋 箱詰め方法

#### 1. 100本入り（2L・L）
- **下段**: 50本を並べます。
- **仕切り**: 新聞紙を挟みます。
- **上段**: 下段と**反対向き**に50本を並べます。
- **合計**: 100本

#### 2. 80本入り（2L80）
- 下段40本、上段40本の計80本。

#### 3. M規格（160本）
- 下段80本、上段80本の計160本。

---

### ✨ 荷姿の整え方
- **花頭の整列**: 花頭を完璧に揃えます。上から見て均一に見えるように整えるのが基本です。
- **入りにくい場合**: 1束ずつずらしながら入れ、無理に押し込まないようにします。

---

### ✅ 最終チェック事項（目視確認）
- [ ] **花傷みの有無**: 梱包中に傷をつけていないか。
- [ ] **葉の擦れ防止**: 束同士の重なりは適正か。
- [ ] **食害の確認**: 虫や食痕が残っていないか。
- [ ] **病斑の確認**: 最終段階で病気の兆候がないか。

---

### 📉 未実施・不備のリスク
- 輸送中の花傷み、市場評価の低下。
- 箱間違いや病害虫混入による重大クレームの発生。

### 💎 難易度と姿勢
- **難易度**: **Level 3**
- 迅速さも重要ですが、出荷工程では何よりも**「正確性」**が最優先されます。
`.trim()

    await prisma.workManual.update({
        where: { id: shippingId },
        data: {
            purpose: '等級ごとに正しく箱詰めし、輸送中の花傷みを防ぎ、市場評価を維持する最後の品質管理。',
            timingStandard: '水揚げ後。正確性重視。',
            actionSteps: richMarkdown,
            riskIfNotDone: '輸送中の花傷み、クレーム、市場評価低下、病害虫混入',
            requiredTime10a: 26,
            difficultyLevel: 3,
            impactOnQuality: '箱詰め時の美しい荷姿と花頭の揃いが、市場価格（単価）に直結する',
            impactOnYield: '箱間違い防止による出荷ロスの低減',
            impactOnProfit: '信用の維持と市場評価の最大化。クレームによる廃棄ロスの防止',
            inputParameters: JSON.stringify({
                boxes: ['15cm箱', '13cm箱'],
                packing_counts: ['100本', '80本', '160本'],
                checks: ['花傷み', '葉擦れ', '食害', '病斑']
            })
        }
    })

    console.log(`Updated "Shipping" manual with rich markdown: ${shippingId}`)
}

main()
    .catch(console.error)
    .finally(async () => {
        await prisma.$disconnect()
    })
