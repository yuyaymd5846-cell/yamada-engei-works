
const { PrismaClient } = require('@prisma/client')

const prisma = new PrismaClient()

async function main() {
    const stickingId = '4c15c261-0e14-4fbf-9161-49f65fa4f6ba'

    const richMarkdown = `
### 🎯 作業目的
均質な生育のために、**発根状況が揃った苗**を準備します。

#### 🚀 目標
- **Lv.1 (基本)**: 8分/枚
- **Lv.2 (熟練)**: 5分/枚
- 手順と注意点を完全に理解し、効率化を目指す。

---

### 🛠 準備するもの
- [ ] **穂木**
- [ ] **薬液**（ビーナイン、オキシベロン、ファンタジスタ、アドマイヤー）
- [ ] **ソイルブロック**

---

### 📋 手順と注意点

#### 1. 品種と苗の確認
- [ ] 作業前に**品種**と**必要枚数**を確認します。
- > **⚠️ 注意**: 採穂から**20日以上経過**したものは、黄化（葉が黄色くなる現象）がないか細かくチェックしてください。

#### 2. 穂木の準備
- 穂木を薬液に浸し、空のコンテナにあげてしっかりと水を切ります。

#### 3. 挿し作業のコツ
- ソイルブロックに穂木を挿します。
- **深さ**: 穂木の基部が**約1cm程度**埋まるようにします。
- > **重要**: 穂木がソイルブロックの底を**突き抜けないよう**、力加減に注意してください。

#### 4. トレイの配置と乾燥防止
- 挿し終わったブロックトレイを並べます（3トレイで1列になるように揃える）。
- > **⚠️ 乾燥注意**: トレイが並び始めたら、乾燥を防ぐために**速やかにポリ（ビニール）をかけます**。
- 品種が切り替わる際は、最後のトレイに**品種名の札**を立て、掃除を徹底して混ざらないようにします。

#### 5. 仕上げ（密封）
- すべての挿し芽を終えたら、ポリを全体にしっかりとかぶせます。
- > **⚠️ 鉄則**: ポリの端はトレイの下にしっかりと挟み込んでください。**隙間があると乾燥し、発根しません。**

---

### 📉 未実施・不備のリスク
- 発根ムラ、乾燥による枯死、品種混ざり、生育の不揃い

### 📅 作業後の流れ
- 作業完了後、速やかに適切な環境（育苗エリア）へ管理を移行します。
`.trim()

    await prisma.workManual.update({
        where: { id: stickingId },
        data: {
            purpose: '均質な生育のために発根状況が揃った苗で準備する。',
            timingStandard: '目標: 5〜8分/枚、手順と注意点の理解',
            actionSteps: richMarkdown,
            riskIfNotDone: '発根ムラ、乾燥による枯死、品種混ざり、生育の不揃い',
            requiredTime10a: 46,
            difficultyLevel: 3,
            inputParameters: JSON.stringify({
                chemicals: ['ビーナイン', 'オキシベロン', 'ファンタジスタ', 'アドマイヤー'],
                tools: ['ソイブロック', 'ブロックトレイ', 'ポリ']
            })
        }
    })

    console.log(`Updated "Sticking Cuttings" manual with rich markdown: ${stickingId}`)
}

main()
    .catch(console.error)
    .finally(async () => {
        await prisma.$disconnect()
    })
