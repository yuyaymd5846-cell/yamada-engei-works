
const { PrismaClient } = require('@prisma/client')

const prisma = new PrismaClient()

async function main() {
    const cleanupId = '93acd6e6-ba00-4397-b65a-93e51414f5a8'

    const richMarkdown = `
### 🎯 作業目的
次作へ最短で切り替えるため、段取り替え時間を極限まで短縮します。ハウスをリセットし、即座に土壌消毒・施肥へ移行できる状態を作ります。
> **💡 片付けは「次作のスタート速度」を決める極めて重要な工程です！**

#### 🚀 目標
- 収穫終了後、速やかに圃場を空にする。
- ネット・杭・ダクトを安全かつ効率的に回収する。
- 茎残渣を粉砕し、次工程へ即移行可能な状態にする。

---

### 📋 基本方針
- **「一斉に、短時間で終わらせる」**: 迷わず流れ作業で進めます。
- **逆算思考**: 次作の定植日から逆算してスケジュールを組みます。
- **全体利益の優先**: 収穫が遅れすぎる場合は、出荷を切り上げる判断も必要です。
  - > ※ 次作への切り替え遅れによる損失（機械費・固定費等）を考慮してください。

---

### 📋 作業手順

#### 1. 未収穫株の処理
- 生育遅れ株をまとめて切除します。
- 次作のスケジュールに影響する場合は、思い切って終了判断を下します。

#### 2. ネット回収
- 上綱がほどけないよう注意しながら、丁寧にたぐります。
- **ポイント**: 後の補助準備で伸ばしやすいよう整理し、大きなカラビナ等で一括固定します。
- > **⚠️ 注意**: ネットは引っかかりやすいため、無理に引かないでください。

#### 3. 杭抜き
- 親杭は深く打たれているため、**専用の杭抜き機（オレンジ色）**を使用します。
- 無理な力作業を避け、抜いた杭はハウスの側面柱へ立てかけて整理します。

#### 4. 加温ダクト処理（冬期）
- ※下吹きダクト設置時のみ実施。
- 加温機で一度送風して膨らませ、ダクト上の泥を落としてから畳んで収納します。

#### 5. ハンマーナイフモア作業
- 残茎を粉砕し、土壌へすき込みます。
- **完了合図**: この作業が終われば、即日または翌日の「土壌消毒」「施肥」への移行が可能です。

---

### 📉 未実施・遅延のリスク
- **次作の遅れ**: 年間作付回数の減少、固定費回収不足、利益の圧迫。
- **病害リスク**: 残渣放置による病害虫の持ち越し。
- **作業効率低下**: ネット処理不良による次作準備の停滞。

### 💎 品質・利益への影響
- **利益**: 段取り替えの1日の遅れが、年間利益（数十万〜数百万円）に直結します。
- > **鉄則**: 片付けは「コスト工程」ではなく、**「回転率を上げる利益工程」**として捉えてください。
`.trim()

    await prisma.workManual.update({
        where: { id: cleanupId },
        data: {
            purpose: '次作へ最短で切り替え、段取り替え時間を短縮、ハウスをリセットする。',
            timingStandard: '収穫終了後。目標: 10aあたり約6時間。',
            actionSteps: richMarkdown,
            riskIfNotDone: '次作遅れ、年間作付回数減、固定費回収不足、病害リスク',
            requiredTime10a: 6,
            difficultyLevel: 3,
            impactOnQuality: '残渣放置による病害リスク、ネット処理不良による効率低下',
            impactOnYield: '回転率の低下による年間利益の減少（1日の遅れが数万〜数十万円の損失）',
            inputParameters: JSON.stringify({
                tools: ['杭抜き機', 'ハンマーナイフモア', 'カラビナ'],
                notes: ['逆算スケジュール', '最短リセット']
            })
        }
    })

    console.log(`Updated "Cleanup" manual with rich markdown: ${cleanupId}`)
}

main()
    .catch(console.error)
    .finally(async () => {
        await prisma.$disconnect()
    })
